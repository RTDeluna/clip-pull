app = FastAPI()
# CORS stays wide open deliberately: the renderer loads over file:// (an
# origin CORSMiddleware can't reliably allowlist by name across Electron/
# Chromium versions), and restricting this list buys nothing on its own --
# the actual gate against "any webpage the user happens to have open can
# also reach this 127.0.0.1-bound API" is API_TOKEN below, checked on every
# request regardless of origin. See require_api_token.
app.add_middleware(CORSMiddleware, allow_origins=["*"], allow_methods=["*"], allow_headers=["*"])

# A fresh random secret generated by main.js on every app launch (passed
# down via this env var) and required on every request below except
# /health. Binding to 127.0.0.1 alone is NOT sufficient isolation: browsers
# happily let a webpage fetch() a localhost server and, combined with the
# CORS config above, read the response too -- this token is what actually
# stops an arbitrary website from reading API keys out of /settings,
# writing files via /queue, or wiping /history while the app is running.
# Unset (local `python main.py` runs outside Electron, and the pytest
# suite, neither of which go through main.js) means auth is a no-op --
# logged loudly since that should never be true for a real user's install.
API_TOKEN = os.environ.get("CLIP_PULL_API_TOKEN")

