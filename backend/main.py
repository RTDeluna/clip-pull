async def _revalidate_license(store: LicenseStore) -> None:
    # Opportunistic re-check of a cached-active license, run as a fire-and-forget
    # startup task. verify_license does a blocking httpx call, so -- exactly like
    # transcription's AI-client calls -- it runs in a thread executor to avoid
    # blocking the event loop. A network failure leaves the cached status alone
    # (offline grace: don't revoke Pro just because the user is offline); only a
    # definitive answer from Gumroad updates the cache.
    license_key = store.get_license_key()
    if not license_key:
        return
    # Same dev-only bypass as the /license/activate route (see
    # license_config.py) -- without this, a license activated locally via
    # CLIP_PULL_DEV_LICENSE_KEY gets silently revoked on the very next
    # backend restart, since Gumroad has never heard of the dev key and
    # would otherwise reject it here.
    if DEV_LICENSE_KEY and license_key == DEV_LICENSE_KEY:
        store.touch_validated()
        return
    loop = asyncio.get_running_loop()
    try:
        result = await loop.run_in_executor(
            None, functools.partial(verify_license, license_key, increment_uses_count=False)
        )
    except GumroadClientError as exc:
        logger.info("Skipping license revalidation -- couldn't reach Gumroad (%s).", exc)
        return

    purchase = result.get("purchase") or {}
    if not result.get("success") or (
        purchase.get("refunded") or purchase.get("chargebacked") or purchase.get("disputed")
    ):
        store.set_invalid()
    else:
        store.touch_validated()


@app.middleware("http")
async def require_api_token(request: Request, call_next):
    # No token configured -- see API_TOKEN's comment above: this is expected
    # for the pytest suite and a manual `python main.py` run, never for the
    # packaged/Electron-launched app, which always sets one.
    if not API_TOKEN:
        return await call_next(request)
    # /health is polled by main.js before the window (and its token) even
    # exists, and reveals nothing beyond "the process is up". CORS preflight
    # (OPTIONS) requests never carry the app's custom header by spec -- they
    # carry no route logic of their own either, so exempting them is safe;
    # CORSMiddleware (registered first, so it wraps this middleware) answers
    # those directly in the common case anyway.
    if request.url.path == "/health" or request.method == "OPTIONS":
        return await call_next(request)
    if request.headers.get("x-clip-pull-token") != API_TOKEN:
        return JSONResponse(status_code=401, content={"detail": "Missing or invalid API token."})
    return await call_next(request)


@app.get("/health")
def health() -> dict:
    return {"status": "ok"}


@app.websocket("/ws")
async def websocket_endpoint(websocket: WebSocket) -> None:
    # BaseHTTPMiddleware (require_api_token above) only wraps the HTTP
    # request/response cycle -- WebSocket connections bypass it entirely, so
    # this route checks the same token itself. Browsers' WebSocket
    # constructor can't set custom headers, so the token rides as a query
    # param instead (see frontend/ws-client.js).
    if API_TOKEN and websocket.query_params.get("token") != API_TOKEN:
        await websocket.close(code=1008)  # 1008 = policy violation
        return
    await connection_manager.connect(websocket)
    try:
        # Kept inside the try so a disconnect during this initial sync send
        # is caught like any other, and the finally below still removes the
        # connection from the broadcast list.
        await websocket.send_json({"type": "sync", "entries": queue_manager.to_list()})
        while True:
            await websocket.receive_text()
    except WebSocketDisconnect:
        pass
    except Exception:
        # Any other transport-level error (not a clean WebSocketDisconnect)
        # still needs the connection removed from the broadcast list -- left
        # unhandled, broadcast()'s own dead-connection cleanup would
        # eventually self-heal this, but only on the next message sent.
        pass
    finally:
        connection_manager.disconnect(websocket)


if __name__ == "__main__":
    import uvicorn

    if not API_TOKEN:
        logger.warning(
            "CLIP_PULL_API_TOKEN is not set -- API authentication is DISABLED. "
            "Every route (including plaintext AI provider keys in /settings) is "
            "reachable by anything that can reach 127.0.0.1:8934, including a "
            "webpage open in the user's browser. This is expected for a manual "
            "`python main.py` run or the test suite; it must NEVER happen for "
            "the packaged app, which always sets this env var itself."
        )

    if not check_ffmpeg_available():
        logger.warning(
            "ffmpeg not found on PATH. Downloads will use a single "
            "pre-muxed format instead of the highest available quality."
        )

    reset_count = history_store.reset_stuck_transcriptions()
    if reset_count:
        logger.warning(
            "Reset %d transcription(s) left stuck on 'running' from a previous session.",
            reset_count,
        )

    # A real HTTP re-check can't run in this synchronous pre-startup block
    # without blocking app launch, so schedule it as a fire-and-forget task
    # once the event loop is running -- and only if a license is cached active.
    @app.on_event("startup")
    async def revalidate_license_on_startup() -> None:
        if license_store.get()["status"] == "active":
            track_task(asyncio.create_task(_revalidate_license(license_store)))

    # main.js's exit handler already sees this process's exit code/signal,
    # but not *why* -- a packaged Windows GUI app has no console, so without
    # this, an unhandled exception here (or uvicorn failing to even bind)
    # left nothing behind to diagnose beyond "it stopped." Logged through the
    # existing `logger` so it lands in clippull.log alongside everything
    # else that was happening right before the crash, then re-raised so the
    # process still exits non-zero exactly as before -- this only adds a
    # paper trail, it doesn't change the failure behavior itself.
    try:
        uvicorn.run(app, host="127.0.0.1", port=8934)
    except OSError:
        logger.exception(
            "uvicorn.run() failed to start -- port 8934 may already be in use "
            "by another process (a stuck previous instance, or a second copy "
            "of CLIP.PULL already running)."
        )
        raise
    except Exception:
        logger.exception("Backend crashed while running uvicorn.run().")
        raise
